#依次打印列表中的各个数据
#while
name_list = ['Tom','Lily','Rose']
#循环变量
i = 0
#循环条件-变量小于列表长度
while i < len(name_list):
    #循环体
    print(name_list[i])
    #循环变量发生变化
    i += 1

#for
for i in name_list:
    print(i)

